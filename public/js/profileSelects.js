var getSelectListFor = function(elem){
	var res = '';
	if(!current_lang || current_lang === 'fr'){
		if(elem === '.js-select-year'){
			for(var i=1960; i<(new Date()).getYear()+1900;i++){
				res+='<option>'+i+'</option>';
			}
			res+='<option>maintenant</option>';
		}else{
			selects_fr[elem].forEach(function(opt){
				res+='<option>'+opt+'</option>';
			});
		}
		return res
	}else if(current_lang === 'en'){
		if(elem === 'js-select-year'){
			for(var i=1960; i<(new Date()).getYear()+1900;i++){
				res+='<option>'+i+'</option>';
			}
			res+='<option>now</option>';
		}else{
			selects_en[elem].forEach(function(opt){
				res+='<option>'+opt+'</option>';
			});
		}
	}
}

