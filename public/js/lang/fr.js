function getTextForId(id) {
    switch (id) {
    case 'text_Label':
        return "Label";
    case 'text_description':
        return "description";
    case 'text_reconnaissance':
        return "reconnaissance";
    case 'text_Examen':
        return "Examen";
    case 'text_inscription_etc':
        return "inscription, <br/>date, lieu & prix";
    case 'text_Indexation':
        return "Indexation";
    case 'text_comment':
        return "comment?";
    case 'text_pour_qui':
        return "pour qui?";
    case 'text_Witken':
        return "Witken";
    case 'text_nous':
        return "nous";
    case 'text_nos_partenaires':
        return "nos partenaires";
    case 'text_Language_switch':
        return "English";
    case 'text_Language_current':
        return "francais";
    case 'text_Login':
        return "Login";
            
    }
}